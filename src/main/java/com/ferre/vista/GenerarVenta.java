    private void Btn_addProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_addProductoActionPerformed
        // TODO add your handling code here:
        if(this.getCodigo().getText()!="" && this.getCantProducto().getText()!=""){
            try{
                Productos product = productosctrl.getById(Integer.valueOf(this.getCodigo().getText()));
                if(product.getExistencia()<Integer.valueOf(this.getCantProducto().getText())){
                    JOptionPane.showMessageDialog(this,"El producto seleccionado no cuenta con suficientes unidades disponibles", "Error"
                    , JOptionPane.ERROR_MESSAGE);
                }else{
                    ordenarProducto(product, Integer.valueOf(getCantProducto().getText()));
                    generarTotal();
                    this.gettotal().setText(String.valueOf(total));
                
                }
                
                
            }
            
            catch(Exception c){
                JOptionPane.showMessageDialog(this,"El codigo que ingreso no pertenece a ningun producto", "Error"
                    , JOptionPane.ERROR_MESSAGE);
            }
            
        }else{
            JOptionPane.showMessageDialog(this,"Rellene correctamente el formulario", "Advertencia"
                    , JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Btn_addProductoActionPerformed

    private void btn_facturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_facturarActionPerformed
        // TODO add your handling code here:
       if(this.getIdCliente().getText()!=""&& this.getNombreCl().getText()!=""){
           int ganancias = this.gananciasTotales();
           Date date = new Date();
           Cliente cliente = clientesctrl.getById(Integer.valueOf(this.getIdCliente().getText()));
           Factura factura = new Factura(null,cliente,date,
                   Double.valueOf(this.gettotal().getText()),ganancias);
           facturacontrolador.save(factura, tofactura);
       }
    }//GEN-LAST:event_btn_facturarActionPerformed

    private void btn_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_borrarActionPerformed
        // TODO add your handling code here:
        int fila=tbl_venta.getSelectedRow();
        
        if(fila>=0)
        {
            
            
            int response = JOptionPane.showConfirmDialog(this, "¿Quieres eliminar este elemento?", "Advertencia",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(response==JOptionPane.YES_OPTION){
                int row = fila; 
                productos.remove(row);
                tofactura.remove(row);
                limpiarTabla();
                generarTabla();
                generarTotal();
                this.gettotal().setText(String.valueOf(total));
                
            }        
             
             
        }
        else
        {
           JOptionPane.showMessageDialog(this,"Selecciona un producto", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_btn_borrarActionPerformed

