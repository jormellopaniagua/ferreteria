    public void ordenarProducto(Productos p, Integer cantidad){
        
            int dinero = (int) (p.getPrecioVenta() * cantidad);
            Object [] dato = new Object[7];
            dato[0] = p.getCodigo();
            dato[1] = p.getNombre();
            dato[2] = p.getMedida();
            dato[3] = p.getTipo();
            dato[4] = p.getPrecioVenta();
            dato[5] =  cantidad;
            dato[6] = dinero;
            productos.add(dato);
            TOFactura tof = new TOFactura(p.getCodigo(),cantidad,p.getPrecioVenta()*cantidad,((p.getPrecioVenta()*cantidad)-(p.getPrecioCompra()*cantidad)));
            tofactura.add(tof);
             ((DefaultTableModel)tbl_venta.getModel()).addRow(dato);
            
    }
    public void generarTabla(){
    
        for(Object ob:productos){
            ((DefaultTableModel)tbl_venta.getModel()).addRow((Object[]) ob);
            
        }
    }
    public void generarTotal()
    {
        total = 0;
        int subTotal =0;
        int fila = tbl_venta.getRowCount();
        if(fila>0){
            for (int i=0;i<=fila-1;i++)
        {
            Object dinero=((DefaultTableModel)tbl_venta.getModel()).getValueAt(i, 6);
            String din = String.valueOf(dinero);
            subTotal = Integer.valueOf(din);
           
            total += subTotal;
             
            
            
        }
        }
        
        
    }
    public void limpiarTabla()
    {
        int fila = tbl_venta.getRowCount();
        for (int i=fila-1;i>=0;i--)
        {
            ((DefaultTableModel)tbl_venta.getModel()).removeRow(i);
        }
    }
    public Integer gananciasTotales(){
        int dinero = 0;
        for(TOFactura f:tofactura){
            dinero += f.getGanancia();
        }
        return dinero;
    }
    
