    public void setCampos(Empleados e){
            this.getCedula().setText(e.getIdCedula().toString());
            this.getNombre().setText(e.getNombreEmpleado());
            this.getNacimiento().setDate(e.getFechaNacimiento());
            this.getcontraseña().setText(e.getContrasena());
            this.getDireccion().setText(e.getDireccion());
            this.getTelefono().setText(e.getTelefono());
           
            
            if(e.getCargo()=="Administrador"){
                this.radio_admin.setSelected(true);
            }else{
                this.radio_empleado.setSelected(true);
            }
    }

