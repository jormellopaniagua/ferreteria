    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        // TODO add your handling code here:
        Empleados empleado = new Empleados();
        if(this.getCedula().getText()!= "" && this.getNombre().getText()!= "" && String.valueOf(this.getNacimiento().getDate())!= ""
            && this.getcontraseña().getText()!="" && this.getTelefono().getText()!="" && getDireccion().getText() != ""
                && this.radio_admin.isSelected()!=false || this.radio_empleado.isSelected()!=false)
        {
            
            try{
            empleado.setIdCedula(Integer.valueOf(this.getCedula().getText()));
            empleado.setNombreEmpleado(this.getNombre().getText());
            empleado.setFechaNacimiento(this.getNacimiento().getDate());
            empleado.setContrasena(this.getcontraseña().getText());
            empleado.setDireccion(this.getDireccion().getText());
            empleado.setTelefono(this.getTelefono().getText());
            
            if(this.radio_admin.isSelected()){
                empleado.setCargo("Administrador");
            }else{
                empleado.setCargo("Empleado");
            }
            
            
                try{
                   
                        
                        empleadosctrl.getById(Integer.valueOf(this.getCedula().getText()));
                        empleadosctrl.actualizar(empleado);
                        this.setVisible(false);
                        new EmpleadosV().setVisible(true);
                          
        
                }catch(Exception e){
                        JOptionPane.showMessageDialog(null,"La cedula no se encuentra  registrada", "Advertencia"
                        , JOptionPane.WARNING_MESSAGE);
                }
            }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Rellene correctamente el formulario", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
            }
        }else{
            JOptionPane.showMessageDialog(this,"Todas las casillas deben ser llenadas correctamente", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_btn_ActualizarActionPerformed

    

