    public void leerDatos(){
        List<Empleados> Empleados = empleadosctrl.getAll();
        for (Empleados e: Empleados){
            Object [] dato = new Object[5];
            dato[0] = e.getIdCedula();
            dato[1] = e.getNombreEmpleado(); 
            dato[3] = e.getDireccion();
            dato[4] = e.getTelefono();
            try{
                SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
                String date = format.format(e.getFechaNacimiento());
                dato[2] = date;
            }catch(Exception x){
                dato[2] = e.getFechaNacimiento();   
            }
            
            
            
                
             ((DefaultTableModel)tbl_Empleado.getModel()).addRow(dato);
        }
    }
    public void limpiarTabla()
    {
        int fila = tbl_Empleado.getRowCount();
        for (int i=fila-1;i>=0;i--)
        {
            ((DefaultTableModel)tbl_Empleado.getModel()).removeRow(i);
        }
    }


