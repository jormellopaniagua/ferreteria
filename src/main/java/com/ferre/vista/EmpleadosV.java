    private void Btn_consEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_consEmpleadoActionPerformed
        // TODO add your handling code here:
        if(this.getide()!=null){
            try{
                Empleados empleado = empleadosctrl.getbyId(Integer.valueOf(this.getide()));
                Object [] dato = new Object[5];
                dato[0] = empleado.getIdCedula();
                dato[1] = empleado.getNombreEmpleado(); 
                dato[3] = empleado.getDireccion();
                dato[4] = empleado.getTelefono();
                    try{
                        SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
                        String date = format.format(empleado.getFechaNacimiento());
                        dato[2] = date;
                    }catch(Exception x){
                        dato[2] = empleado.getFechaNacimiento();   
                    }
                JOptionPane.showMessageDialog(this,"Se encontro un empleado", "Exitoso"
                    , JOptionPane.INFORMATION_MESSAGE);
                limpiarTabla();
                ((DefaultTableModel)tbl_Empleado.getModel()).addRow(dato);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"No se encontro ningun empleado", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
            }
            
            
        }else{
            JOptionPane.showMessageDialog(this,"Por favor escriba una cedula", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
        }
      
        
    }//GEN-LAST:event_Btn_consEmpleadoActionPerformed

    private void Btn_actEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_actEmpleadoActionPerformed
        // TODO add your handling code here:
        int fila=tbl_Empleado.getSelectedRow();
        
        if(fila>=0)
        {
            
            Integer codigo = (Integer)tbl_Empleado.getValueAt(fila, 0);
            Empleados empleado  = empleadosctrl.getbyId(codigo);
            this.setVisible(false);
            new ActualizarEmpleado(empleado).setVisible(true);
                    
             
             
        }
        else
        {
           JOptionPane.showMessageDialog(this,"Selecciona un empleado", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_Btn_actEmpleadoActionPerformed

    private void Btn_delEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_delEmpleadoActionPerformed
        // TODO add your handling code here:
        int fila=tbl_Empleado.getSelectedRow();
        
        if(fila>=0)
        {
            
            Integer codigo = (Integer)tbl_Empleado.getValueAt(fila, 0);
            int response = JOptionPane.showConfirmDialog(this, "Â¿Quieres eliminar este elemento?", "Advertencia",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(response==JOptionPane.YES_OPTION){
                Empleados emple = new Empleados(codigo);
                empleadosctrl.eliminar(emple);
                this.limpiarTabla();
                this.leerDatos();
            }        

                    
             
             
        }
        else
        {
           JOptionPane.showMessageDialog(this,"Selecciona un empleado", "Advertencia"
                    , JOptionPane.WARNING_MESSAGE); 
        }
    
    }//GEN-LAST:event_Btn_delEmpleadoActionPerformed
        
